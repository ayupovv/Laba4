package Laba4;
import java.awt.event.*;
import javax.swing.*;

public class ListenerLogo implements ActionListener{
	private Image logo;
    private JFrame frame;
    private JPanel panel;
    public ListenerLogo(JFrame frame, JPanel panel, Image logo) {
    	this.frame = frame;
    	this.panel = panel;
    	this.logo = logo;
    }
    @Override
    public void actionPerformed(ActionEvent e) {
    	panel.add(logo);
    	logo.setBounds(250,30,150,150);
    	}
}